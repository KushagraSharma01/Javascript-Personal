// Important methods
// push,pop,shift,unshift

const colors=['red','blue','green','purple'];
//here colors stores the reference to the actual array 
colors.push('grey','brown');//inserts at last
console.log(colors);
console.log(colors.pop()); //removes value from last
console.log(colors);
colors.shift(); //removes value from start
console.log(colors);
colors.unshift('black');//inserts at front
console.log(colors);
